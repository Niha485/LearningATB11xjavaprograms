package ex_02_literals;

public class Lab006_Literals {
    public static void main(String[] args) {
        int age = 65;
              age = 66; // here 65 and 66 are literals

//          1. Boolean Literals - true, false
//          2. Character Literals ('A', '9', '\n')
//          3. String Literals ("Hello", "Java")
//          4. Integral Literals(Integer values)
//          5. Floating Literals(3.14, 2.53)
//         6. null Literal (absence of valuee), Scientific Literals (1.2e5, 3.6E-4) represents small or big numbers
        System.out.println(age);
    }
}
