package ex_09_For_loop;

public class Lab040_Using_break {

    public static void main(String[] args) {
        // break - it is used to come out of the loop

        for (int i = 0; i < 50; i++) {
            if (i == 5) {
                break;
            }
                System.out.println(i);
            }
        }
    }


