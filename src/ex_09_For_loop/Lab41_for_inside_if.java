package ex_09_For_loop;

public class Lab41_for_inside_if {
        public static void main(String[] args) {
            for(int i = 0; i < 18; i++){
                if(i > 15){
                    System.out.println("You will get gift of iPhone");
                }
                else{
                    System.out.println("No gift");
                }
            }
        }
    }

