package ex_02_Operators;

public class Lab012_unary_operator {
    public static void main(String[] args) {
        // Unary Operator
        int a = +10;
        int a1 = -20;
        int result = a + a1;
        System.out.println(a);
        System.out.println(a1);
        System.out.println(result);
        int b = -2;
        b = b + 2;
        System.out.println(b);

    }
}

