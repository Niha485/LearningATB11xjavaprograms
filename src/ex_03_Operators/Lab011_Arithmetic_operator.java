package ex_03_Operators;

public class Lab011_Arithmetic_operator {
    public static void main(String[] args) {

        // Arithmetic Operators
        //        + (Addition)
        //       - (Subtraction)
        //       * (Multiplication)
        //       / (Division) Quotient
        //       % (Modulus) | Modulus - Remainder

        int a = 17;
        int b = 4;
        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);
        System.out.println(a%b);
    }
}

